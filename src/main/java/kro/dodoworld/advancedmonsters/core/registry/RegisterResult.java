package kro.dodoworld.advancedmonsters.core.registry;

/**
 * Represents register result for registrable class.
 */
public enum RegisterResult {
    /**
     * Represents successful register result.
     */
    SUCCESS,
    /**
     * Represents failure register result.
     */
    FAIL,
    /**
     * Represents an unknown register result.
     */
    UNKNOWN
}
